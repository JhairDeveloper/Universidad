    public void guardar(){
        if(txtNombre.getText().isEmpty() || txtDiaNacimiento.getText().isEmpty() || txtMesNacimiento.getText().isEmpty()
           || txtAnioNacimiento.getText().isEmpty()){
            JOptionPane.showMessageDialog(null, "Ingrese los datos", "Error", JOptionPane.ERROR_MESSAGE);
        }else{
            Persona p = new Persona(txtNombre.getText(), (TipoGenero) cbxGeneros.getSelectedItem(), obtenerFechaNacimiento());
            personaDao.setPersona(p);
            try {
                if(personaDao.guardar()){
                    JOptionPane.showMessageDialog(null, "Se ha registrado");
                }
            } catch (Exception e) {
                JOptionPane.showMessageDialog(null, e.getMessage(), "Error desconocido", JOptionPane.ERROR_MESSAGE);
            }
        }
    }
    public Date obtenerFechaNacimiento(){
        Date dateFormat =  new Date ((Integer.parseInt(txtAnioNacimiento.getText())-1900), (Integer.parseInt(txtMesNacimiento.getText())-1),  Integer.parseInt(txtDiaNacimiento.getText()));
        System.out.println(dateFormat);
        return dateFormat; 
    }

