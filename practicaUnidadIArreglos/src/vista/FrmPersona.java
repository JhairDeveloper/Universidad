    private void btnSeleccionarPersonaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSeleccionarPersonaActionPerformed
        fila = tblPersonas.getSelectedRow();
        if(fila>=0){
            pc.setPersona(mtp.getPersonas()[fila]);
            txtNombrePersona.setText(pc.getPersona().getNombre());
            txtAnio.setText(String.valueOf(pc.getPersona().getFechaNacimiento().getYear()));
            txtMes.setText(String.valueOf(pc.getPersona().getFechaNacimiento().getMonth()));
            txtDia.setText(String.valueOf(pc.getPersona().getFechaNacimiento().getDate()));
        }else{
            JOptionPane.showMessageDialog(null, "Selecione el dato de la tabla", "Error", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btnSeleccionarPersonaActionPerformed

    private void btnModificarPersonaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnModificarPersonaActionPerformed
        if(pc.getPersona().getNombre().equalsIgnoreCase(PersonaControlador.NCOMUN)){
            Integer i = JOptionPane.showConfirmDialog(this, "Esta seguro que quiere eliminar los datos?");
            if(i == JOptionPane.OK_OPTION){
                cambiarPersona();
            }
        }else{
            cambiarPersona();
        }
    }//GEN-LAST:event_btnModificarPersonaActionPerformed

